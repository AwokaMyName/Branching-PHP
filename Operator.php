<?php
$a = 4;
$b = 50;

# operator aritmatika standar
echo "Aritamtika <hr>";
echo "{$a} + {$b} = " . ($a + $b) . " <br>";
echo "{$a} - {$b} = " . ($a - $b) . " <br>";
echo "{$a} * {$b} = " . ($a * $b) . " <br>";
echo "{$a} / {$b} = " . ($a / $b) . " <br>";
echo "<br>";

# modulus
echo "Modulus <hr>";
echo "{$a} % {$b} = " . ($a % $b) . " <br>";
echo "{$b} % {$a} = " . ($b % $a) . " <br>";
echo "<br>";

# eksponensial
echo "Eksponensial <hr>";
echo "{$a} * {$b} = " . ($a * $b) . " <br>";
echo "{$b} * {$a} = " . ($b * $a) . " <br>";
echo "<hr>";

# negasi
echo "Negasi <br>";
echo "-a = " . (-$a) . " <br>";
echo "-b = " . (-$b) . " <br>";
echo "<hr>";